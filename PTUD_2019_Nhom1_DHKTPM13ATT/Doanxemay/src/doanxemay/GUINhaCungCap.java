    private void showDuLieu(){
        Connection connection= Connect.getConnect();
            String query="SELECT * from dbo.[XeMay] x join dbo.[NhaCungCap] n on x.maNhaCungCap=n.maNhaCungCap join dbo.[LoaiXe] l on l.maLoaiXe=x.maLoaiXe";
        try {           
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {        
                this.lblmancc.setText(rs.getString("maNhaCungCap"));                   
                this.txttenncc.setText(rs.getString("tenNhaCungCap"));
                this.txtsodienthoai.setText(rs.getString("soDienThoai"));
                    
            }
            
        } catch (Exception e) {
            
        }
    }
