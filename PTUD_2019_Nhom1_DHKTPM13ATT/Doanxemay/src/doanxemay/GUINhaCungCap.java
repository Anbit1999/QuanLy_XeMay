    private void txttimkiemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txttimkiemKeyReleased
        if(txttimkiem.getText().length()==0){
            DanhsachNhaCungCap.loadDuLieuNhaCungCap(sql, tblNhaCungCap);
        }
        else{         
            if(cmbloaitk.getSelectedItem()=="Tên nhà cung cấp"){
                String sqltenncc = "Select * from dbo.[NhaCungCap] where tenNhaCungCap like '%"+this.txttimkiem.getText()+"%' ";
                DanhsachNhaCungCap.timtenNhaCungCap(sqltenncc, tblNhaCungCap, this.txttimkiem.getText());
            }
            if(cmbloaitk.getSelectedItem()=="Số điện thoại"){
                String sqlsdt = "Select * from dbo.[NhaCungCap] where soDienThoai like '%"+this.txttimkiem.getText()+"%' ";
                DanhsachNhaCungCap.timsdtNhaCungCap(sqlsdt, tblNhaCungCap, this.txttimkiem.getText());
            }
        }
    }//GEN-LAST:event_txttimkiemKeyReleased

