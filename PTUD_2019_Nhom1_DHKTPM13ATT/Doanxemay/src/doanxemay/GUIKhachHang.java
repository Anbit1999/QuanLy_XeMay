    /**
     * Lưu thông tin khách hàng sau khi sửa hoặc thêm
     * 
     * Ngày: 4/10/2019
     * 
     * @author : Phan Duy Tuấn
     */
    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        SimpleDateFormat sdf= new SimpleDateFormat("dd/MM/yyyy");
        /*java.util.Date ngaysinh =  this.Chon_ngaysinh.getDate();
        Date ns = (Date) ngaysinh;*/
        java.util.Date date = Chon_ngaysinh.getDate();
        String ngaysinh = DateFormat.getDateInstance().format(date);
        int error=0;
        if(this.txttenkh.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Tên khách hàng không được bỏ trống");
            error=1;
        }
        else{
            error=0;
        }
        int error2=0;
        if(this.txtsodienthoai.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Số điện thoại không được bỏ trống");
            error2=1;
        }
        else{
            if(this.txtsodienthoai.getText().length()!=10){
                JOptionPane.showMessageDialog(this, "Số điện thoại phải có 10 kí tự số");
                error2=1;
            }
            else{
                error2=0;
            }
            
        }
        int error3=0;
        if(this.txtcmnd.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Số CMND không được bỏ trống");
            error3=1;
        }
        else{
            if(txtcmnd.getText().length()==9 || txtcmnd.getText().length()==13){
                error3=0;
            }
            else{
                JOptionPane.showMessageDialog(this, "Số CMND phải có 9 hoặc 13 kí tự số");
                error3=1;
            }
        }
        if(error==1 || error2==1 || error3==1){
            if(this.lblmakh.getText()=="Tự động phát sinh"){
                JOptionPane.showMessageDialog(this, "Thêm thông tin khách hàng thất bại");
            }
            else{
                JOptionPane.showMessageDialog(this, "Sửa thông tin khách hàng thất bại");
            }
        }
        else{
            if(this.lblmakh.getText()=="Tự động phát sinh"){
                DanhsachKhachHang.ThemKhachHang(makh, this.txttenkh.getText(), this.txtsodienthoai.getText(), this.txtcmnd.getText()
                        ,ngaysinh, this.txtemail.getText());
                DanhsachKhachHang.loadDuLieuKhachHang(sql, tblKhachHang);
            }
            else{
                if(this.lblmakh.getText().length()==0){
                    JOptionPane.showMessageDialog(null, "Bạn phải chọn khách hàng cần sửa","Thông báo",1);
                }
                else{
                    if(JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn sửa khách hàng có mã là "+makh+" hay không ?","Thông báo",2)==0){
                        DanhsachKhachHang.SuaKhachHang(makh, this.txttenkh.getText(), this.txtsodienthoai.getText(), this.txtcmnd.getText(), ngaysinh, this.txtemail.getText());
                        DanhsachKhachHang.loadDuLieuKhachHang(sql, tblKhachHang);
                    }

                }

            }
        }
        
    }//GEN-LAST:event_btnluuActionPerformed
