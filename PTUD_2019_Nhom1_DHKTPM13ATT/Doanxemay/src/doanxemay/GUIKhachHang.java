    private void showDuLieu(){
        Connection connection= Connect.getConnect();
            String query="SELECT * from dbo.[KhachHang]";
        try {           
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {        
                this.lblmakh.setText(rs.getString("maKhachHang"));                   
                this.txttenkh.setText(rs.getString("tenKhachHang"));
                this.Chon_ngaysinh.setDate(rs.getDate("ngaySinh"));
                this.txtsodienthoai.setText(rs.getString("soDienThoai"));
                this.txtcmnd.setText(rs.getString("soCMND"));
                this.txtemail.setText(rs.getString("email"));
            }
            
        } catch (Exception e) {
            e.printStackTrace(); 
        }
    }
